"""
1. Faça um Programa que peça dois números e imprima o maior deles.
"""

# Solicita os dois valores
valor1 = float(input("Digite o 1º valor: "))
valor2 = float(input("Digite o 2º valor: "))

# Compara e imprime o maior valor
if valor1 > valor2:
    print("O maior valor digitado é:", valor1)
else:
    print("O maior valor digitado é:", valor2)
