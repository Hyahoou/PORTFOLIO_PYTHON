"""
5. Faça um Programa que converta metros para centímetros.

"""
# Resposta 1:
print('Converter de metros para centímetros\n')
valor1 = input('Digite o valor que deseja converter: ')
valor1_convertido = float(valor1)
valor1_centrimetros = int(valor1_convertido*100)
print(f'{valor1} metros é igual a {valor1_centrimetros} centímetros ')