"""
3. Faça um Programa que peça dois números e imprima a soma.
"""

# Resposta 1:
# Solicita ao usuário o primeiro número e converte para tipo float
numero1 = float(input('Digite um número: '))

# Solicita ao usuário o segundo número e converte para tipo float
numero2 = float(input('Digite um número: '))

# Calcula a soma dos dois números e exibe o resultado
print(numero1 + numero2)

# Resposta 2:
