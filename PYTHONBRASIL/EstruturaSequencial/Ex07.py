# CÁLCULO DA ÁREA DO QUADRADO

lado = float(input('Digite o valor do Lado: '))
area = lado**2
dobroarea = area*2
print(f'Valor da Área do QUADRADO é {area:.2f} e o dobro da área do quadrado é: {dobroarea:.2f}')